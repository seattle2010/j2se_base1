public class _5switch {
    public static void main(String[] args) {
        int score = 90;
        switch(score/10){
        case 10:
        case 9:
        System.out.println("优秀");
        break;
        case 8:
        System.out.println("很好");
        break;
        default:
        System.out.println("");
        break;
        }
        
            
    }
}
